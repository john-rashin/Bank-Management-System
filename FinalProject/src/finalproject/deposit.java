    private void submitBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBTNActionPerformed
        // TODO add your handling code here:
         String Amount = amount.getText();
        double Amountd = Double.parseDouble(Amount);
       String getAmount =dip.getSpecificField("SELECT `amount_bal`FROM `account`");
        String getAccNum =dip.getSpecificField("SELECT`account_pin`FROM `account`");
      
      if(accountNum.getText() != getAccNum){ 
       
          double totalBal = Double.parseDouble(getAmount) + Amountd;
           
          dip.executeUpdate("UPDATE `account` SET  `amount_bal`='"+totalBal+"'WHERE `account_pin`='"+accountNum.getText()+"'");
      
         JOptionPane.showMessageDialog(null, "Deposit Successful!");
          // JOptionPane.showMessageDialog(null, "Wrong account number!");
      }else{
       JOptionPane.showMessageDialog(null, "Wrong account number!");
      }
      amount.setText("");
    }//GEN-LAST:event_submitBTNActionPerformed

    private void showBalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showBalActionPerformed
        // TODO add your handling code here:
         String totalBal =dip.getSpecificField("SELECT `amount_bal`FROM `account` WHERE `account_pin`= '"+accountNum.getText()+"'");
        bal.setText(totalBal);
        if(showBal.isSelected()){
            bal.setEchoChar((char)0);
        }  else{
            bal.setEchoChar('*');
        }
         bal.setEditable(false);
    }//GEN-LAST:event_showBalActionPerformed

